def get_month(month_int):
    if month_int == 1:
        return 'января'
    if month_int == 2:
        return 'февраля'
    if month_int == 3:
        return 'марта'
    if month_int == 4:
        return 'апреля'
    if month_int == 5:
        return 'мая'
    if month_int == 6:
        return 'июня'
    if month_int == 7:
        return 'июля'
    if month_int == 8:
        return 'августа'
    if month_int == 9:
        return 'сентября'
    if month_int == 10:
        return 'октября'
    if month_int == 11:
        return 'ноября'
    if month_int == 12:
        return 'декабря'
    else:
        return 'неправильное число месяца'
